<h1>Grote Huisspin</h1>
<p> De grote huisspin (Tegenaria parietina) is een spinnensoort in de taxonomische indeling van de trechterspinnen (Agelenidae).

Het dier behoort tot het geslacht Tegenaria. De wetenschappelijke naam van de soort werd voor het eerst geldig gepubliceerd in 1785 door Fourcroy.
